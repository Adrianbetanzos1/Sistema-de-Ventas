    public void añadirPedidos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID pedido");
        modelo.addColumn("ID producto");
        modelo.addColumn("Nombre producto");
        modelo.addColumn("ID proveedor");
        modelo.addColumn("Nombre proveedor");
        modelo.addColumn("Sucursal");
        modelo.addColumn("Tipo de producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Estado");

        tablaPedidos.setModel(modelo);

        Conexion conn = new Conexion();
        Connection reg = conn.conectar();
        Connection con = reg;

        PreparedStatement ps = null;
        ResultSet rs = null;

        String[] datos = new String[9];

        String sql = "SELECT * FROM pedidos INNER JOIN producto ON pedidos.producto = producto.codigo_producto INNER JOIN proveedor ON pedidos.proveedor = proveedor.codigo_proveedor";

        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(9);
                datos[3] = rs.getString(3);
                datos[4] = rs.getString(11);
                datos[5] = rs.getString(4);
                datos[6] = rs.getString(5);
                datos[7] = rs.getString(6);
                datos[8] = rs.getString(7);
                modelo.addRow(datos);
            }
            tablaPedidos.setModel(modelo);

        } catch (Exception e) {
            System.out.println("Error en la consulta: " + e);

        }
    }
    
    
