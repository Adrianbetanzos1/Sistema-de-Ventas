    private void cancelarPedidoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarPedidoBtnActionPerformed
        MetodosSQL metodos = new MetodosSQL();
        Proveedor proveedor = new Proveedor();
        Sucursal sucursal = new Sucursal();
        Producto producto = new Producto();
        Pedido pedido = new Pedido();
        
        int fila = tablaPedidos.getSelectedRow();
        try{            
            if(fila == -1){
                JOptionPane.showMessageDialog(this, "No se ha seleccionado un pedido", "Pedido no seleccionado", JOptionPane.WARNING_MESSAGE);             
            }else{
                
                String idPedido = tablaPedidos.getValueAt(fila, 0).toString();                
                
                int pedidoInt = Integer.parseInt(idPedido);
                pedido.setId_pedido(pedidoInt);
            
                if(metodos.cancelarPedido(pedido)){                    
                    añadirPedidos();
                    JOptionPane.showMessageDialog(this, "Pedido cancelado correctamente", "Pedido cancelado", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "No se ha podido cancelar el pedido", "Pedido no cancelado", JOptionPane.ERROR_MESSAGE);
                }
            }            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado un pedido", "Pedido no seleccionado", JOptionPane.WARNING_MESSAGE);
        }               
    }//GEN-LAST:event_cancelarPedidoBtnActionPerformed

